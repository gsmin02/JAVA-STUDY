package lotto_project;

public class Member {
	String member_name;
	String member_yy;
	String member_mm;
	String member_dd;
	String member_email;
	String member_address;
	String member_pay;
	
	public void setMember_name(String member_name) { this.member_name = member_name; }
	public void setMember_yy(String member_yy) { this.member_yy = member_yy; }
	public void setMember_mm(String member_mm) { this.member_mm = member_mm; }
	public void setMember_dd(String member_dd) { this.member_dd = member_dd; }
	public void setMember_email(String member_email) { this.member_email = member_email; }
	public void setMember_address(String member_address) { this.member_address = member_address; }
	public void setMember_pay(String member_pay) { this.member_pay = member_pay; }
	
	public String getMember_name() { return member_name; }
	public String getMember_yy() { return member_yy; }
	public String getMember_mm() { return member_mm; }
	public String getMember_dd() { return member_dd; }
	public String getMember_email() { return member_email; }
	public String getMember_address() { return member_address; }
	public String getMember_pay() { return member_pay; }
}
